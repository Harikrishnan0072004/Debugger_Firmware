#ifdef ENABLE_MAGIC_KEY_SWITCH
void process_uart_triggers(struct CDCUartInstance_t* uart, uint8_t* bufferIn, uint32_t bufferSz)
{
    for(int j=0;j<4;j++)
    {
        for(int i=0;i<bufferSz;i++)
        {        
            uint8_t* matchTrigger = uartTriggers[j].key;
          
            uint8_t c = bufferIn[ i ];
            uint8_t currTriggerVal = matchTrigger[ uart->matchKeyStates[j] ];
            if(c == currTriggerVal )
            {
                uart->matchKeyStates[j]++;
            }
            else
            {
                uart->matchKeyStates[j] = 0;
            }

            if( uart->matchKeyStates[j] >= MAGIC_KEY_SIZE)
            {
                uart->matchKeyStates[j] = 0;

                // Set next targe
                uart->toggleNextProfileId = uartTriggers[j].profileId;
            }

        }
    }
}
#endif


void tud_cdc_line_coding_cb(uint8_t itf, cdc_line_coding_t const* line_coding)
{
  if(itf > MAX_UART_DEVICES)
    return;
  struct CDCUartInstance_t* uart = &uartDevices[itf];
  if( !uart->isUartInitialised )
    return;

  switch(uart->type)
  {
    case EUartType_Hardware:
    {
      cdc_uart_line_coding_cb(uart, itf, line_coding);
      break;
    }
    default:
      break;
  }
}


void tud_cdc_line_state_cb(uint8_t itf, bool dtr, bool rts)
{
  if(itf > MAX_UART_DEVICES)
    return;
  struct CDCUartInstance_t* uart = &uartDevices[itf];
  if( !uart->isUartInitialised )
    return;

  switch(uart->type)
  {
    case EUartType_Hardware:
    {
      cdc_uart_line_state_cb(uart, itf, dtr, rts);
      break;
    }
    default:
      break;
  }
}


void cdc_itf_configure(struct CDCUartInstance_t* uart, const char* name, uint8_t itf, enum EUartType type, int32_t defaultProfileId)
{
    uart->interval = 100;
    uart->type = type;
    uart->name = name;
    uart->itf = itf;
    uart->baud = -1;
    uart->rxPin = -1;
    uart->txPin = -1;    
    uart->isConfigured = true;     
    uart->defaultProfileId = defaultProfileId;
    uart->currentProfile = 0; 
    uart->nextProfile = &uartProfiles[defaultProfileId];
}


void cdc_led_init()
{
  for(int i=0;i<MAX_UART_PROFILES;i++)
  {
    struct UartProfile_t* profile = &uartProfiles[i];

    if(profile->activityLedPin == -1)
      continue;

    // Led off
    gpio_init(profile->activityLedPin);
    gpio_set_dir(profile->activityLedPin, GPIO_OUT);    
    gpio_put(profile->activityLedPin, 0);        
  }
}


void cdc_led_update()
{
  for(int i=0;i<MAX_UART_PROFILES;i++)
  {
    struct UartProfile_t* profile = &uartProfiles[i];

    if(profile->activityLedPin == -1)
      continue;

    // find wired uart
    int wiredCount = 0;
    bool hasActivity = false;
    for(int j=0;j<MAX_UART_DEVICES;j++)
    {
      struct CDCUartInstance_t* uart = &uartDevices[j];
      if(uart->currentProfile == profile && uart->isUartInitialised)
      {
        wiredCount++;

        if( (time_us_32() - uart->lastActivityTimeTx < CDC_UART_LED_FLASH_TIME) || (time_us_32() - uart->lastActivityTimeRx < CDC_UART_LED_FLASH_TIME) )
        {
          hasActivity = true;
        }

        break;
      }
    }
    
    // Check if uart is routing to profile ( hw uart port )
    if(wiredCount)
    {
      // led flasher RX or TX
      if(hasActivity)
      {                            
        gpio_set_dir(profile->activityLedPin, GPIO_OUT);    
        gpio_put(profile->activityLedPin, 1);            
      }
      else
      {
        // Init dim (use pull)          
        gpio_set_dir(profile->activityLedPin, GPIO_IN);    
        gpio_pull_up(profile->activityLedPin);    
      }
    }
    else
    {
      // Led off      
      gpio_set_dir(profile->activityLedPin, GPIO_OUT);    
      gpio_put(profile->activityLedPin, 0);    
    }
  }
}
